            <section id="introduction" class="section">
                <h2>Introduction</h2>
                <p>The Central Board of Secondary Education (CBSE) has approved the introduction of open-book exams for Class 9 students starting from the 2026-27 academic session. This move marks a significant shift in the Indian education system, aiming to reduce rote learning and promote analytical and application-based thinking among students.</p>
            </section>

            <section id="what-are-open-book-exams" class="section">
                <h2>What Are Open-Book Exams?</h2>
                <p>Open-book assessments (OBAs) allow students to consult their textbooks, class notes, or other permitted resources while answering exam questions. Unlike traditional exams, they do not focus on memory but instead evaluate how well students can interpret, analyze, and apply concepts in real-world scenarios.</p>
                <div class="highlight-box">
                    "Open-book exams test analysis, not memorization"
                </div>
            </section>

            <section id="why-now-rationale" class="section">
                <h2>Why Now? Rationale Behind CBSE’s Decision</h2>
                <p>This reform is aligned with the National Education Policy (NEP) 2020, which emphasizes competency-based learning over rote memorization. CBSE’s pilot studies have shown that open-book exams encourage students to think critically and apply knowledge creatively, even though initial results highlighted the need for better training and exam design.</p>
            </section>

            <section id="how-will-it-be-conducted" class="section">
                <h2>How Will It Be Conducted?</h2>
                <p>The open-book format will be integrated into three pen-and-paper assessments each term for core subjects such as languages, mathematics, science, and social science. CBSE will provide schools with sample papers and detailed guidelines to ensure smooth implementation. Importantly, adoption will be optional for schools, giving them flexibility in transition.</p>
            </section>

            <section id="lessons-from-the-past" class="section">
                <h2>Lessons from the Past</h2>
                <p>This is not CBSE’s first experiment with open-book assessments. The Open Text-Based Assessment (OTBA) introduced in 2014 was later discontinued due to limited effectiveness. However, the new model incorporates lessons from that initiative, focusing on teacher training, better resource design, and alignment with NEP 2020 to ensure improved outcomes.</p>
            </section>

            <section id="advantages-and-hurdles" class="section">
                <h2>Advantages and Potential Hurdles</h2>
                <p>Experts believe open-book exams will reduce stress, shift focus from memorization to understanding, and nurture critical thinking skills. However, challenges include the need for teachers to prepare more analytical questions, proper training for schools, and maintaining exam integrity.</p>
            </section>

            <section id="conclusion" class="section">
                <h2>Conclusion</h2>
                <p>The CBSE’s decision to introduce open-book exams for Class 9 represents a forward-looking step in transforming education. If implemented effectively, it could reshape the way students engage with knowledge, preparing them for higher studies and real-world problem-solving. The move underscores a broader shift in Indian education towards competency-based and stress-free learning.</p>
