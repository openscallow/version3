                <h2>Phased Implementation Across Classes</h2>
                <p>The integration of the Bhagavad Gita is being carried out in a phased manner. While it is now mandatory for Classes 9 to 12, a supplementary textbook on the Gita was introduced in the previous academic year for students in Classes 6 to 8. This gradual approach ensures that students are introduced to the concepts in an age-appropriate manner, building a foundation for a deeper understanding in higher classes.</p>
                <div class="highlight-box">
                     "The implementation is being done in phases, starting with a supplementary textbook for middle school and now being mandatory for high school."
