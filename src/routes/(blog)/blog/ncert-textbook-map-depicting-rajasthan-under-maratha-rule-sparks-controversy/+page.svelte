            <section id="royal-families-voice-strong-objections" class="section">
                <h2>Royal Families Voice Strong Objections</h2>
                <p>The issue was first brought to public attention by Chaitanya Raj Singh, a member of the former Jaisalmer royal family. He labeled the map "factually incorrect, misleading and agenda-driven" on social media. His stance was quickly supported by other former royal families. Mahima Kumari, a BJP MP and member of the erstwhile Mewar royal family, along with her husband Vishwaraj Singh, also joined the chorus demanding that the NCERT rectify the error to prevent the dissemination of incorrect historical information to students.</p>
            </section>

            <section id="claims-of-historical-inaccuracy" class="section">
                <h2>Claims of Historical Inaccuracy</h2>
                <p>Opponents of the map argue that it presents a flawed understanding of the political landscape of 18th-century India. They contend that historical records clearly show the princely states of Rajasthan maintained their autonomy. Former Union Minister Bhanwar Jitendra Singh, from the Alwar royal family, emphasized that history should be based on verifiable facts, not regional or political agendas. He stated that the map's portrayal of complete Maratha control over Rajasthan is a historical misrepresentation, as the Rajput states were independent entities.</p>
            </section>

            <section id="political-backlash-and-demands-for-correction" class="section">
                <h2>Political Backlash and Demands for Correction</h2>
                <p>The controversy has also taken a political turn, with leaders demanding government intervention. The collective demand from royal families and politicians is for the NCERT to remove the controversial map from the textbook and issue a corrected version. They stress the importance of presenting an accurate and unbiased account of history in educational materials to ensure students receive a fact-based education.</p>
            </section>

            <section id="conclusion" class="section">
                <h2>Conclusion</h2>
                <p>The dispute over the NCERT map highlights the sensitive nature of historical representation in educational textbooks. The strong reactions from Rajasthan's former royal families and political figures have sparked a wider debate on the need for accuracy and the potential for such materials to create a skewed understanding of the past. The incident underscores the importance of a rigorous, evidence-based approach to the writing of history textbooks to avoid such controversies in the future.</p>
