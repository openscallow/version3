            <section id="introduction" class="section">
                <h2>Introduction</h2>
                <p>A new map in an 8th-grade textbook has ignited a debate about historical accuracy. The map, featured in a publication by the NCERT, portrays the region of modern-day Rajasthan as being under the control of the Maratha Empire during the mid-18th century. This has drawn sharp criticism from various quarters, who argue that the depiction is misleading and not based on historical facts, leading to a call for its immediate withdrawal and correction.</p>
            </section>

            <section id="the-maps-depiction" class="section">
                <h2>The Map's Depiction</h2>
                <p>The core of the controversy lies in a single map illustrating the extent of the Maratha Empire in the year 1759. The map includes almost the entirety of what is now Rajasthan within the Maratha dominion. Critics argue that while the Marathas did have influence and conducted raids in the region, the princely states of Rajasthan, including major powers like Mewar, Marwar, Jaipur, and Bikaner, never fully came under Maratha rule and maintained their independence.</p>
