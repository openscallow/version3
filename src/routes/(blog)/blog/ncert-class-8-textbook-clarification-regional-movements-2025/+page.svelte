            <section id="The-New-Textbook-and-Emerging-Concerns" class="section">
                <h2>The New Textbook and Emerging Concerns</h2>
                <p>The introduction of the new NCERT textbook for Class 8 social science, "Exploring Society: India and Beyond," has been met with a mix of anticipation and apprehension. While the new curriculum aims to provide a fresh perspective on India's past, concerns were quickly raised by educators, historians, and various community groups regarding the apparent exclusion of significant regional resistance movements from the initial volume. The absence of these crucial narratives from the textbook sparked a debate about the comprehensiveness and inclusivity of the new curriculum.</p>
            </section>

            <section id="NCERT's-Two-Volume-Approach" class="section">
                <h2>NCERT's Two-Volume Approach</h2>
                <p>In response to the growing concerns, the NCERT has clarified that the current textbook is only the first of a two-part series. The council has explained that the curriculum for Class 8 social science has been designed to be delivered in two volumes. The first volume, which is currently available, lays the foundational context, while the second volume will delve into more specific and regional histories. This two-volume approach has been adopted to ensure a more detailed and in-depth exploration of India's complex past without overburdening students.</p>
            </section>

            <section id="What-to-Expect-in-the-Second-Volume" class="section">
                <h2>What to Expect in the Second Volume</h2>
                <p>The NCERT has provided specific details about the content planned for the second volume of the textbook. It has been confirmed that chapters dedicated to significant regional uprisings will be included. Among the prominent movements that will be featured are the Paika Rebellion of Odisha, a valiant early armed uprising against British rule, and the Kuka Movement of Punjab, a socio-political movement that played a crucial role in the region's history. The second volume is currently under development and is expected to be released between September and October 2025.</p>
            </section>

            <section id="Broader-Curriculum-Overhaul" class="section">
                <h2>Broader Curriculum Overhaul</h2>
                <p>The changes in the Class 8 social science textbook are part of a larger curriculum and textbook renewal process undertaken by the NCERT. The council has recently introduced a series of new textbooks for various subjects across different grades, including new titles for Classes 5 and 8 in subjects like Hindi, English, Art, and Sanskrit. These new textbooks are aligned with the National Education Policy (NEP) 2020 and aim to provide a more holistic and contemporary learning experience for students. The new textbooks are available for purchase on the official NCERT website.</p>
            </section>

            <section id="conclusion" class="section">
                <h2>Conclusion</h2>
                <p>The clarification from the NCERT regarding the two-volume structure of the new Class 8 social science textbook has helped to allay some of the initial concerns. By confirming the inclusion of important regional histories in the forthcoming second volume, the council has reiterated its commitment to presenting a comprehensive and balanced account of India's past. The successful implementation of this two-part series will be crucial in ensuring that students receive a well-rounded education that acknowledges the diverse and multifaceted nature of India's historical landscape.</p>
