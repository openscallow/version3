                <h2>Introduction</h2>
                <p>New Delhi: The National Council of Educational Research and Training (NCERT) has issued a clarification regarding its new Class 8 Social Science textbook, "Exploring Society: India and Beyond," addressing concerns about the omission of several regional resistance movements. The council has assured that these topics will be covered in a forthcoming second volume of the textbook</p>
