                <h2>About the New Books</h2>
                <p>The latest set of textbooks introduces a fresh and engaging approach to learning. For Class 8, students will now have access to "Curiosity," a Science textbook designed to pique their interest in the world around them, and "Kaushal Bodh," a textbook for Vocational Education aimed at developing practical skills. Other new additions for Class 8 include "Malhar" for Hindi, "Kriti" for Art, and "Poorvi" for English.</p>
                <p>Class 5 students are also receiving new learning materials with the introduction of "Veena," a new Hindi textbook, and "Santoor," a new English textbook. These books are designed to be more interactive and student-friendly, encouraging active participation in the learning process.</p>
