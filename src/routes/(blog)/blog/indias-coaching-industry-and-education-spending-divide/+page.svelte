            <section id="introduction" class="section">
                <h2>Introduction</h2>
                <p>In India, a good education is often seen as the ultimate key to a better life. This aspiration drives families to invest heavily in their children's future. A recent analysis sheds light on two critical aspects of this phenomenon: the colossal size of the coaching centre industry and the stark spending disparities between urban and rural households. As we unpack these figures, we reveal a complex picture of ambition, economic pressure, and a widening educational gap across the country.</p>
            </section>

            <section id="The-₹31,000-Crore-Coaching-Juggernaut" class="section">
                <h2>The ₹31,000 Crore Coaching Juggernaut</h2>
                <p>The shadow economy of private coaching has long been a topic of discussion, but recent data provides a staggering official estimate. The coaching centre industry in India is now valued at a massive ₹31,000 crore. This figure is derived from the Goods and Services Tax (GST) receipts, which themselves have shown a remarkable upward trend.</p>
                <div class="highlight-box">
                    "India's coaching industry has ballooned into a ₹31,000 crore behemoth, revealing the immense financial weight of academic ambition in the country"
                </div>
                <p>GST collections from coaching centres climbed from ₹11,357 crore in FY19 to ₹13,462 crore in FY23, indicating robust growth. This financial boom underscores the immense demand for supplementary education as students prepare for hyper-competitive entrance exams. The government's increased scrutiny of the sector for misleading claims and its role in student pressure has done little to slow down this commercial behemoth.</p>
            </section>

            <section id="The-Urban-Rural-Education-Spending-Gap" class="section">
                <h2>The Urban-Rural Education Spending Gap</h2>
                <p>The data reveals a significant divide in how much households spend on education depending on where they live. In 2022-23, urban households spent nearly 6% (5.96%) of their Monthly Per-Capita Consumption Expenditure (MPCE) on education. In stark contrast, rural households spent just under 3.5% (3.49%).</p>
                <p>While spending has increased for both groups since 2011-12, the gap has remained stubbornly wide. This disparity suggests that access to and the ability to afford quality educational resources, including private coaching, are significantly greater in cities. This financial gap can easily translate into an opportunity gap for students from rural backgrounds.</p>
                <div class="highlight-box">
                    "A stark educational divide persists in India, with urban households spending nearly 6% of their monthly budget on education—almost double the rate of their rural counterparts."
                </div>
             </section>

