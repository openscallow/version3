            <section id="How-to-Participate-and-Submission-Guidelines" class="section">
                <h2>How to Participate and Submission Guidelines</h2>
                <p>The competition is open to all citizens, and entries can be submitted through the MyGov India platform. The submission period is from July 28, 2025, to August 14, 2025. Here are some of the key guidelines for your submission:</p>

                <ul style="list-style-position: inside;">
                    <li><b>Simplicity and Uniqueness:</b> The logo should be easily recognizable and unique.</li>
                    <li><b>Scalability:</b> The design must be clear and effective at all sizes.</li>
                    <li><b>Color Palette:</b> The colors should reflect the vibrant culture and heritage of Gujarat.</li>
                    <li><b>Typography:</b> Fonts used should be legible and appropriate.</li>
                    <li><b>File Formats:</b> Submissions must be in both vector (SVG, EPS, PDF) and raster (PNG, JPG) formats, with high resolution (300 DPI for print and 72 DPI for digital).</li>
                </ul>
